local banners = {
  ['basic'] = {
    '==================================================================',   '==================================================================',
    '==================================================================',
    '==================================================================',
    '==================================================================',
    '==================================================================',
    '========   _______ __              __         __         =========',
    '========  |    ___|  |.-----.----.|  |_.----.|__|.----.  =========',
    '========  |    ___|  ||  -__|  __||   _|   _||  ||  __|  =========',
    '========  |_______|__||_____|____||____|__|  |__||____|  =========',
    '========    _______               __                     =========',
    '========   |_     _|.---.-.-----.|  |_.----.---.-.       =========',
    '========     |   |  |  _  |     ||   _|   _|  _  |       =========',
    '========     |___|  |___._|__|__||____|__| |___._|       =========',
    '========                                                 =========',
    '==================================================================',
