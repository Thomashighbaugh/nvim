    '==================================================================',
    '==================================================================',
    '=================================================================='
  }
}

return setmetatable(
  banners,
  {
    __index = function(logos, key)
      if key == 'random' then
        local keys = vim.tbl_keys(logos)
        return logos[keys[math.random(1, #keys + 1)]]
      end
      return logos[key]
    end
  }
)

